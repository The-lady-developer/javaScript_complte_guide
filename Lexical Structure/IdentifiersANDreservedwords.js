//*Identifier*//
//An identifier is simply a name. In JavaScript, identifiers are used to name variables and functions and to provide labels for certain loops in JavaScript code.
//General started with letters, digits, _, @, $ sign.
//Digits are not allowed as the first character so that JavaScript can easily distinguish identifiers from numbers
//below are the valid literals
i
my_variable_name
v13
_dummy
$str
//For portability and ease of editing, it is common to use only ASCII letters and digits in identifiers.
//This allows programmers to use variable names from non-English languages and also to use mathematical symbols:
var sí = true;
var π = 3.14;

//*Reserved words* //
//JavaScript reserves a number of identifiers as the keywords of the language itself. You cannot use these words as identifiers in your programs:
